def count_letters(text):
    text = text.lower() # опускаем буквы вниз
    alphabet = {} # список букв
    for i in range(len(text)): # по тексту ищем буквы и суммируем их
        if text[i].isalpha():
            if text[i] in alphabet:
                alphabet[text[i]] += 1 # хотел так но если не было буквы то ошибка
            else:
                alphabet[text[i]] = 1 # нужно если новая буква. см выше
    return alphabet
def calculate_frequency(dict_alphabet):
    sum = 0
    for bukva in dict_alphabet: # сначала сумму букв
        sum += dict_alphabet[bukva]
    for bukva in dict_alphabet: # частота
        print(f"{bukva}: {dict_alphabet[bukva]/sum:.2f}")

